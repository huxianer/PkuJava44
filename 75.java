public class Solution {
    public void sortColors(int[] nums) {
        //实质问题就是对nums进行升序排序
        //使用冒泡排序法
        for(int i = 0; i < nums.length - 1; i++){
            for(int j = 0; j < nums.length - i - 1; j++){
                if(nums[j] > nums[j+1]){
                    int temp = nums[j];
                    nums[j] = nums[j+1];
                    nums[j+1] = temp;
                }
            }
        }
    }
}